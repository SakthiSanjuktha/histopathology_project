import { useLocation, useNavigate } from 'react-router-dom';
import { useEffect, useState } from 'react';
import './Results.css';

function Results() {
  const location = useLocation();
  const navigate = useNavigate();
  const [result, setResult] = useState(null);

  useEffect(() => {
    if (location.state) {
      setResult(location.state);
    } else {
      navigate('/prediction');
    }
  }, [location, navigate]);

  if (!result) {
    return null;
  }

  const isMalignant = result.prediction === 'Malignant';

  return (
    <div className="results fade-in">
      <h1 className="page-title">Prediction Results</h1>
      <p className="page-subtitle">
        AI-powered analysis of your histopathology image
      </p>

      <section className="section">
        <div className="results-container">
          <div className="result-grid">
            <div className="result-image-card">
              <h3 className="card-title">Analyzed Image</h3>
              <img src={result.image} alt="Analyzed tissue" className="analyzed-image" />
              <p className="image-timestamp">
                Analyzed on: {new Date(result.timestamp).toLocaleString()}
              </p>
            </div>

            <div className="result-details-card">
              <h3 className="card-title">Classification Result</h3>
              
              <div className={`result-indicator ${isMalignant ? 'malignant' : 'benign'}`}>
                <div className="result-icon">{isMalignant ? '⚠️' : '✅'}</div>
                <div className="result-classification">
                  <div className="classification-label">Diagnosis</div>
                  <div className="classification-value">{result.prediction}</div>
                </div>
              </div>

              <div className="confidence-section">
                <h4>Confidence Score</h4>
                <div className="confidence-bar">
                  <div 
                    className={`confidence-fill ${isMalignant ? 'malignant' : 'benign'}`}
                    style={{ width: `${result.confidence * 100}%` }}
                  ></div>
                </div>
                <div className="confidence-value">
                  {(result.confidence * 100).toFixed(1)}%
                </div>
              </div>

              <div className="interpretation-card">
                <h4>Interpretation</h4>
                <p className="interpretation-text">
                  {isMalignant 
                    ? 'The model has detected characteristics consistent with malignant (cancerous) tissue. The image shows abnormal cellular structures and irregular tissue architecture.' 
                    : 'The model has classified this tissue as benign (non-cancerous). The cellular structures appear normal with organized tissue architecture.'}
                </p>
              </div>

              <div className="disclaimer-card">
                <p>
                  <strong>⚠️ Medical Disclaimer:</strong> This result is generated by an AI model for 
                  research purposes only. It should not be used as the sole basis for clinical decisions. 
                  Always consult with qualified healthcare professionals for proper diagnosis and treatment.
                </p>
              </div>
            </div>
          </div>

          <div className="action-buttons">
            <button className="btn btn-primary" onClick={() => navigate('/prediction')}>
              🔄 Analyze Another Image
            </button>
            <button className="btn btn-secondary" onClick={() => window.print()}>
              📄 Print Report
            </button>
          </div>

          <div className="additional-info">
            <h3>Detailed Analysis Metrics</h3>
            <div className="metrics-table">
              <div className="metric-row">
                <span className="metric-name">Model Architecture:</span>
                <span className="metric-value">CNN (Convolutional Neural Network)</span>
              </div>
              <div className="metric-row">
                <span className="metric-name">Input Resolution:</span>
                <span className="metric-value">224×224 pixels</span>
              </div>
              <div className="metric-row">
                <span className="metric-name">Processing Time:</span>
                <span className="metric-value">~2 seconds</span>
              </div>
              <div className="metric-row">
                <span className="metric-name">Model Accuracy:</span>
                <span className="metric-value">96.5%</span>
              </div>
            </div>
          </div>
        </div>
      </section>
    </div>
  );
}

export default Results;
